// Function to display the login iframe
function openLoginIframe() {
    // Set the display property of the login iframe container to 'block'
    document.getElementById('loginiframeContainer').style.display = 'block';
}

// Function to hide the login iframe
function closeLoginIframe() {
    // Set the display property of the login iframe container to 'none'
    document.getElementById('loginiframeContainer').style.display = 'none';
}

